import jsPDF from 'jspdf';
import type { SubCategory, Snippet } from '../lib/cheatsheets';

export async function generateCheatsheetPDF(
  categoryName: string,
  subCategory: SubCategory,
  customTitle?: string,
  customSubtitle?: string
) {
  const pdf = new jsPDF({
    orientation: 'portrait',
    unit: 'mm',
    format: 'a4',
  });

  const pageWidth = 210; // A4 width in mm
  const pageHeight = 297; // A4 height in mm
  const margin = 20;
  const contentWidth = pageWidth - 2 * margin;

  // Page 1: Cover Page
  addCoverPage(
    pdf,
    categoryName,
    subCategory.name,
    pageWidth,
    pageHeight,
    customTitle,
    customSubtitle
  );

  // Middle Pages: Content
  pdf.addPage();
  const lastYPosition = addContentPages(pdf, subCategory, margin, contentWidth, pageHeight);

  // If the last content page ended up being blank, remove it.
  if (lastYPosition <= margin) {
    pdf.deletePage(pdf.getNumberOfPages());
  }

  // Last Page: Advertisement
  pdf.addPage();
  addAdvertisementPage(pdf, pageWidth, pageHeight);

  // Download the PDF
  pdf.save(`${categoryName}-${subCategory.name}-Cheatsheet.pdf`);
}

function addCoverPage(
  pdf: jsPDF,
  categoryName: string,
  subCategoryName: string,
  pageWidth: number,
  pageHeight: number,
  customTitle?: string,
  customSubtitle?: string
) {
  // Background - solid blue
  pdf.setFillColor(37, 99, 235);
  pdf.rect(0, 0, pageWidth, pageHeight, 'F');

  // Lighter blue top section for gradient effect
  pdf.setFillColor(59, 130, 246);
  pdf.rect(0, 0, pageWidth, pageHeight / 2, 'F');

  // Decorative circles with lighter blue
  pdf.setFillColor(96, 165, 250);
  pdf.circle(30, 30, 40, 'F');
  pdf.circle(pageWidth - 30, pageHeight - 30, 50, 'F');

  const centerY = pageHeight / 2;

  pdf.setTextColor(255, 255, 255);
  pdf.setFontSize(64);
  pdf.setFont('helvetica', 'bold');
  pdf.text('CheatJS', pageWidth / 2, centerY - 40, { align: 'center' });

  pdf.setDrawColor(255, 255, 255);
  pdf.setLineWidth(0.5);
  pdf.line(pageWidth / 2 - 30, centerY - 30, pageWidth / 2 + 30, centerY - 30);

  const title = customTitle || categoryName;
  pdf.setFontSize(32);
  pdf.setFont('helvetica', 'bold');
  pdf.text(title, pageWidth / 2, centerY + 5, { align: 'center' });

  const subtitle = customSubtitle || subCategoryName;
  pdf.setFontSize(24);
  pdf.setFont('helvetica', 'normal');
  pdf.text(subtitle, pageWidth / 2, centerY + 25, { align: 'center' });

  pdf.setFontSize(14);
  pdf.setFont('helvetica', 'italic');
  pdf.setTextColor(226, 232, 240);
  pdf.text('Your Quick Reference Guide', pageWidth / 2, centerY + 45, { align: 'center' });

  pdf.setFontSize(11);
  pdf.setFont('helvetica', 'normal');
  pdf.setTextColor(255, 255, 255);
  const linkText = 'Generated by https://cheatjs.vercel.app';
  const linkY = pageHeight - 15;

  pdf.textWithLink(linkText, pageWidth / 2, linkY, {
    align: 'center',
    url: 'https://cheatjs.vercel.app'
  });
}

function addContentPages(
  pdf: jsPDF,
  subCategory: SubCategory,
  margin: number,
  contentWidth: number,
  pageHeight: number
) {
  let yPosition = margin;
  const maxY = pageHeight - margin;

  const processSnippets = (snippets: Snippet[]) => {
    for (const snippet of snippets) {
      yPosition = addSnippetToPDF(pdf, snippet, margin, contentWidth, yPosition, maxY);
      yPosition += 10; // Spacing between snippets
    }
  };

  if (subCategory.sections) {
    for (const section of subCategory.sections) {
      const sectionHeaderHeight = 15;
      if (yPosition + sectionHeaderHeight > maxY) {
        pdf.addPage();
        yPosition = margin;
      }
      pdf.setFontSize(18);
      pdf.setFont('helvetica', 'bold');
      pdf.setTextColor(59, 130, 246);
      pdf.text(section.heading, margin, yPosition);
      yPosition += 10;

      processSnippets(section.snippets);
    }
  }

  if (subCategory.snippets) {
    processSnippets(subCategory.snippets);
  }

  return yPosition;
}

