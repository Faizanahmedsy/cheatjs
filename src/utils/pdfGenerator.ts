function addSnippetToPDF(
  pdf: jsPDF,
  snippet: Snippet,
  margin: number,
  contentWidth: number,
  startY: number,
  maxY: number
): number {
  let yPosition = startY;

  // Helper to add a new page if content doesn't fit
  const checkAndAddPage = (requiredHeight: number) => {
    if (yPosition + requiredHeight > maxY) {
      pdf.addPage();
      yPosition = margin;
    }
  };

  // --- Title ---
  checkAndAddPage(10);
  pdf.setFontSize(14);
  pdf.setFont('helvetica', 'bold');
  pdf.setTextColor(0, 0, 0);
  pdf.text(snippet.title, margin, yPosition);
  yPosition += 8;

  // --- Description ---
  pdf.setFontSize(10);
  pdf.setFont('helvetica', 'normal');
  pdf.setTextColor(80, 80, 80);
  const descLines = pdf.splitTextToSize(snippet.description, contentWidth);

  for (const line of descLines) {
    checkAndAddPage(5);
    pdf.text(line, margin, yPosition);
    yPosition += 5;
  }
  yPosition += 3;

  // --- Code Block ---
  if (snippet.code) {
    const codeLineHeight = 4;
    const codeFontSize = 8;
    const codePadding = 2;

    pdf.setFontSize(codeFontSize);
    pdf.setFont('courier', 'normal');
    pdf.setTextColor(0, 0, 0);

    const allLines = snippet.code.split('\n');
    const wrappedLines: string[] = [];
    allLines.forEach(line => {
      wrappedLines.push(...pdf.splitTextToSize(line, contentWidth - codePadding * 2));
    });

    let lineBuffer: string[] = [];

    const drawBuffer = () => {
      if (lineBuffer.length === 0) return;

      const bufferHeight = lineBuffer.length * codeLineHeight;
      checkAndAddPage(bufferHeight + 5);

      // 1. DRAW BACKGROUND FIRST
      pdf.setFillColor(245, 245, 245);
      pdf.rect(margin, yPosition - 2, contentWidth, bufferHeight + 4, 'F');

      // 2. DRAW TEXT ON TOP
      for (const line of lineBuffer) {
        pdf.text(line, margin + codePadding, yPosition);
        yPosition += codeLineHeight;
      }
      lineBuffer = []; // Clear buffer
    };

    for (const line of wrappedLines) {
      const requiredHeight = (lineBuffer.length + 1) * codeLineHeight;
      if (yPosition + requiredHeight > maxY) {
        drawBuffer();
      }
      lineBuffer.push(line);
    }

    // Draw any remaining lines in the buffer
    drawBuffer();
  }

  return yPosition;
}

